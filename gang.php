									<h2> current numbers</h2>

									<p> </p>

									<p>In 2007, there were approximately 785,000 active street gang members in the United States, according to the National Youth Gang Center. In 2011, the National Gang Intelligence Center of the Federal Bureau of Investigation asserted that "There are approximately 1.4 million active street, prison, and outlaw gang members comprising more than 33,500 gangs in the United States." Approximately 230,000 gang members were in U.S. prisons or jails in 2011.

According to the Chicago Crime Commission publication, "The Gang Book 2012", Chicago has the highest number of gang members of any city in the United States: 150,000 members.[16] Traditionally Los Angeles County has been considered the Gang Capital of America, with an estimated 120,000 (41,000 in the City) gang members.

There were at least 30,000 gangs and 800,000 gang members active across the USA in 2007. About 900,000 gang members lived "within local communities across the country," and about 147,000 were in U.S. prisons or jails in 2009. By 1999, Hispanics accounted for 47% of all gang members, Blacks 31%, Whites 13%, and Asians 7%.

In December 13, 2009, The New York Times published an article about growing gang violence on the Pine Ridge Indian Reservation estimated that there were 39 gangs with 5,000 members on that reservation alone.
</p>

									<h2>types and culture</h2>
									<p> </p>
									<p>Many types of gangs make up the general structure of an organized group.[35]

There are street gangs, with members of similar background and motivations.[36] The term "street gang" is commonly used interchangeably with "youth gang", referring to neighbourhood or street-based youth groups that meet "gang" criteria. Miller (1992) defines a street gang as "a self-formed association of peers, united by mutual interests, with identifiable leadership and internal organization, who act collectively or as individuals to achieve specific purposes, including the conduct of illegal activity and control of a particular territory, facility, or enterprise."[37]

Understanding the structure of gangs is a critical skill to defining the types of strategies that are most effective with dealing with them, from the at-risk youth to the gang leaders.[38] Not all individuals who display the outward signs of gang membership are actually involved in criminal activities. An individual's age, physical structure, ability to fight, willingness to commit violence, and arrest record are often principal factors in determining where an individual stands in the gang hierarchy; now money derived from criminal activity and ability to provide for the gang also impacts the individual's status within the gang. The structure of gangs varies depending primarily on size which can range in size from five or ten to thousands. Many of the larger gangs break up into smaller groups, cliques or sub-sets. The cliques typically bring more territory to a gang as they expand and recruit new members. Most gangs operate informally with leadership falling to whoever takes control; others have distinct leadership and are highly structured, which resembles more or less a business or corporation.

</p>

								
