									<p>The word "gang" derives from the past participle of Old English gan, meaning "to go". It is cognate with Old Norse gangster, meaning "journey." It typically means a group of people, and may have neutral, positive or negative connotations depending on usage.</p>
									<h2>history </h2>
									<p> </p>
									<p>In discussing the banditry in American history Barrington Moore, Jr. suggests that gangsterism as a "form of self-help which victimizes others" may appear in societies which lack strong "forces of law and order"; he characterizes European feudalism as "mainly gangsterism that had become society itself and acquired respectability".

A wide variety of gangs, such as the Order of Assassins, the Damned Crew, Adam the Leper's gang, Penny Mobs, Indian Thugs, Chinese Triads, Snakehead, Japanese Yakuza, Irish mob, Pancho Villa's Villistas, Dead Rabbits, American Old West outlaw gangs, Bowery Boys, Chasers, the Italian mafia, Jewish mafia, and Russian Mafia crime families have existed for centuries. According to some estimates the Thuggee gangs in India murdered 1 million people between 1740 and 1840.

The 17th century saw London "terrorized by a series of organized gangs", some of them known as the Mims, Hectors, Bugles, and Dead Boys. These gangs often came into conflict with each other. Members dressed in the following way: "with colored ribbons to distinguish the different factions."

Chicago had over 1,000 gangs in the 1920s. These early gangs had reputations for many criminal activities, but in most countries could not profit from drug trafficking prior to drugs being made illegal by laws such as the 1912 International Opium Convention and the 1919 Volstead Act.[citation needed] Gang involvement in drug trafficking increased during the 1970s and 1980s, but some gangs continue to have minimal involvement in the trade.

In the United States, the history of gangs began on the East Coast in 1783 following the American Revolution. The emergence of the gangs was largely attributed to the vast rural population immigration to the urban areas. The first street-gang in the United States, the 40 Thieves, began around the late 1820s in New York City. The gangs in Washington D.C. had control of what is now Federal Triangle, in a region then known as Murder Bay.</p>
