									<p>To begin with, not all homicides are crimes. Homicides include all killings of humans. Many homicides, such as murder and manslaughter, violate criminal laws. Others, such as a killing committed in justified self-defense, are not criminal. Illegal killings range from manslaughter to murder, with multiple degrees of each representing the gravity of the crime.</p>
									<h2>Types of murders </h2>
									<p> </p>
									<p>There are thousands of types of murders today. Below is an overview of common cases.</p>
									<p> </p>

									<h2>murder</h2>

									<p> First degree murder is the most serious criminal homicide. Typically, first degree murder is both intentional and premeditated. Premeditated can mean anything from a long time plan to kill the victim, to a shorter term plan. The intent of the accused murderer does not need to be focused on the actual victim. If someone planned on killing one victim, but by accident kills someone else, the murder is still intentional and premeditated meaning a first degree murder charge. Here is more information about your state's first degree murder laws.

When there is a lack of premeditation but the killer intended to kill for example, in homicides commonly described as occurring "in the heat of passion" the homicide may draw second degree murder charges or perhaps voluntary manslaughter charges, depending on the state. Here is more information about your state's second degree murder laws.</p>

									<p> </p>

									<h2>manslaughter</h2>

									<p>Manslaughter generally means an illegal killing that falls short of murder. The lowest form of manslaughter is involuntary manslaughter. This means that the perpetrator did not intend to kill anyone, but still killed the victim through behavior that was either criminally negligent or reckless. One common example is a DUI accident which kills someone. Someone driving drunk is behaving in a criminally reckless manner, even if they had no intent to kill anyone. Here is more information about your state's involuntary manslaughter laws.

Voluntary manslaughter usually means that the offender did not have a prior intent to kill such as when the homicide occurs "in the heat of passion" and without forethought. Depending on the state, this crime may fall under a variant of murder charges, instead of manslaughter. Here is more information about your state's voluntary manslaughter laws.</p>

									<h2>legal homicides</h2>

									<p> </p>

									<p>Some homicides are not illegal. Criminal laws carve out exceptions for some killings which would otherwise fall under criminal laws against manslaughter or murder. These are referred to as "justified homicide". One primary example is a killing in justified self-defense or defense of someone else. Such a homicide is deemed justified if the situation called for self-defense and state law allows lethal force in that type of situation. Most state laws allow justified homicide to defend oneself or another from credible threat of serious crimes such as rape, armed robbery and murder.</p>

									

									<h2>related wrongful dethcrimes</h2>
									<p></p>
									<p>No matter where a homicide falls on the criminal spectrum, it may also bring a civil lawsuit for wrongful death. In the case of a homicide, the family of the victim may sue the alleged perpetrator to collect damages for that person causing the death of their loved one. While wrongful death lawsuits offer monetary results rather than criminal punishment, they also have a much lower standard of proof than the criminal standard of guilt beyond a reasonable doubt.</p>

									




								
