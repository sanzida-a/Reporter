									<p> Lack of consent is key to the definition of rape.[3] Consent is not always expressed verbally, and may instead be implied from the context and from the relationship between the parties, but the absence of objection does not of itself constitute consent.[34] Lack of consent may result from either forcible compulsion by the perpetrator or an incapacity to consent on the part of the victim (such as persons who are asleep, intoxicated or otherwise mentally compromised).[35] Sexual intercourse with a person below the age of consent, i.e., the age at which legal competence is established, is referred to as statutory rape.[3]

Duress is the situation when the person is threatened by force or violence, and may result in the absence of an objection to sexual activity. This can lead to the presumption of consent.[35] Duress may be actual or threatened force or violence against the victim or someone close to the victim. Even blackmail may constitute duress. Abuse of power may constitute duress. For instance, in the Philippines, a man commits rape if he engages in sexual intercourse with a woman "By means of fraudulent machination or grave abuse of authority".[36] The International Criminal Tribunal for Rwanda in its landmark 1998 judgment used a definition of rape which did not use the word 'consent': "a physical invasion of a sexual nature committed on a person under circumstances which are coercive."[37]

Marital rape, or spousal rape, is non-consensual sex in which the perpetrator is the victim's spouse. It is a form of partner rape, domestic violence, and sexual abuse. Once widely accepted or ignored by law, spousal rape is now denounced by international conventions and is increasingly criminalized. Still, in many countries, spousal rape either remains legal, or is illegal but widely tolerated and accepted as a husband's prerogative. In 2006, the UN Secretary-General's In-depth study on all forms of violence against women stated that (pg 113): "Marital rape may be prosecuted in at least 104 States. Of these, 32 have made marital rape a specific criminal offence, while the remaining 74 do not exempt marital rape from general rape provisions. Marital rape is not a prosecutable offense in at least 53 States. Four States criminalize marital rape only when the spouses are judicially separated. Four States are considering legislation that would allow marital rape to be prosecuted."[38] Since 2006, several other states have outlawed marital rape (for example Thailand in 2007[39]). In the US, the criminalization of marital rape started in the mid-1970s and in 1993 North Carolina became the last state to make marital rape illegal.[40] In many countries, it is not clear if marital rape may or may not be prosecuted under ordinary rape laws. In the absence of a spousal rape law, it may be possible to bring prosecution for acts of forced sexual intercourse inside marriage by prosecuting, through the use of other criminal offenses (such as assault based offenses), the acts of violence or criminal threat that were used to obtain submission.[41]

