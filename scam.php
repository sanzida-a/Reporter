									<p>A scam is a term used to describe any fraudulent business or scheme that takes money or other goods from an unsuspecting person. With the world becoming more connected thanks to the Internet, online scams have increased, and it's often up to you to help stay cautious with people on the Internet.</p>
									<h2>Types of computer and Internet related scams </h2>
									<p> </p>
									<p>There are thousands of types of scams today, but most boil down to stealing money, property, or information. Below is an overview of common scams.</p>
									<p> </p>

									<h2>Phishing</h2>

									<p> Receive an e-mail from someone pretending to be your bank indicating you are overdrawn or made a purchase you really didn't make and asking you to log in and verify the information. However, the link in the e-mail actually points to a fake site that logs your username and password information. See our <a href="https://www.computerhope.com/jargon/p/phishing.htm">phising</a> page for further information, real examples, and related links.</p>

									<p> </p>

									<h2>Auction Fraud</h2>

									<p>Someone selling something on an <a href="https://www.computerhope.com/jargon/o/oauction.htm">online auction site</a> such as E-bay or Craigslist that appears to be something it really isn't. For example, someone may claim to be selling tickets for an upcoming concert that really are not official tickets.</p>

									<h2> donation scam</h2>

									<p> </p>

									<p>A person claiming they have or have a child or someone they know with an illness and need financial assistance. Although many of these claims can be real there are also an alarming amount of people who create fake accounts on donation sites in the hope of scaming people out of money.</p>

									<h2>catfish</h2>
									<p> </p>
									<p>A person who creates a fake online profile with the intention of deceiving someone. For example, a woman could create a fake profile on an online dating website, create a relationship with one or more people and then create a fake scenario that asks others for money. See the catfish definition for further information and related links. See our <a href="https://www.computerhope.com/jargon/c/catfish.htm">catfish</a> page for further information.</p>

									<h2>cold-call scam</h2>
									<p></p>
									<p>Someone claiming to be from technical support from a computer company like Dell claims they have received information that your computer is infected with a virus or has been hacked and offers to remotely connect to your computer and fix the problem. Additional information about this type of scam is found on our does Dell call you document.</p>

									<h2>419</h2>
									<p></p>
									<p>419 or the Nigeria scam gives the impression you can gain a large amount of money and only requires bank information to deposit the money into your account. In reality, the bank information is used against the person or the deposits are kept with no reward. See the <a href="https://www.computerhope.com/jargon/num/419.htm">419</a> page for additional information.</p>





								
